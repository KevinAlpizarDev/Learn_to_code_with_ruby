# Integer Methods in Ruby
#
# This script demonstrates various methods available for Integer objects in Ruby.
#
# Methods demonstrated:
# - `next`: Returns the next integer.
# - `succ`: Alias for `next`, returns the next integer.
# - `pred`: Returns the previous integer.
# - `even?`: Checks if the integer is even.
# - `odd?`: Checks if the integer is odd.
# - `to_s`: Converts the integer to a string.
# - `to_f`: Converts the integer to a float.
# - `abs`: Returns the absolute value of the integer.
# - `zero?`: Checks if the integer is zero.
#
# Example usage:
#   puts 10.next  # Output: 11
#   puts -1.next  # Output: 0
#   puts 10.succ  # Output: 11
#   puts 1.pred   # Output: 0
#   puts 5.even?  # Output: false
#   puts 4.odd?   # Output: false
#   puts 5.to_s   # Output: "5"
#   puts 5.to_f   # Output: 5.0
#   puts -5.abs   # Output: 5
#   puts 0.zero?  # Output: true

puts 10.next 
puts -1.next 
puts 10.succ
puts 1.pred




