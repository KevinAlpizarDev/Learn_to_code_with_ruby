# metodo predicado tambien es conocido como metodo booleando, es simplemente un metodo que devuelve un valor booleano como true or false, 

# Ejemplo eres esto? tienes esto? falta esto?

# Normalmente lleva un question mark o signo de interrogacion, no tiene significato tecnico,solo comunica que devuelve un boolean (Conversion por la comunidad)

# Ejemplos 

puts 10.odd? # SI ES IMPAR
puts 3.odd?
puts 10.odd? # Verifica si el número 10 es impar. Devolverá 'false' porque 10 es par.
puts 3.odd?  # Verifica si el número 3 es impar. Devolverá 'true' porque 3 es impar.

# Par
puts 1.even? # Verifica si el número 1 es par. Devolverá 'false' porque 1 es impar.
puts 1.even?

puts 2.positive?

puts -2.negative?


