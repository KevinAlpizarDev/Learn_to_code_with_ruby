# Metodos utiliados para fscilitar las operaciones matematicas de manera facil

puts 3 + 3 # Es una operacion valida 
# Al hacer esa operacion, ruby por detras invoca al metodo +() 
puts 3.+(3) # Esto es otra muestra de que en ruby todo es un objeto 

puts 3.-(3)
puts 3.*(3)
puts 3./(3)
puts 10.div(4)

# Integers 
puts 10.5 / 4.5
puts 10.5./(4.5)
